
$(document).on('page:load',function() {
   $('.fadeIn').hide().delay(300).fadeIn(2000,function(){



  });
	   $(window).bind('scroll', function() {
     var h = $('nav').height();
	   var navHeight = $( window ).height()-h*3;
			 if ($(window).scrollTop() > navHeight) {
				 $('nav').addClass('fixed');
			 }
			 else {
				 $('nav').removeClass('fixed');
			 }
		});

});
